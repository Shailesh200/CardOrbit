/** Deduplicate by slug — first entry wins (flagship metadata preserved). */
export function buildMerchantCatalog(): MerchantCatalogDef[] {
  const seen = new Set<string>();
  const result: MerchantCatalogDef[] = [];
  for (const def of allDefs) {
    if (seen.has(def.slug)) continue;
    seen.add(def.slug);
    result.push(def);
  }
  return result;
}

