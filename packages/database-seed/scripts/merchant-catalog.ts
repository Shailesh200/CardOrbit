export type MerchantCatalogDef = {
  name: string;
  slug: string;
  primaryCategoryCode: string;
  brandName?: string;
  parentBrand?: string;
  website?: string;
  tags?: string[];
  popularityScore?: number;
};

function slugify(name: string): string {
  return name
    .toLowerCase()
    .replace(/&/g, 'and')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
}

function m(
  name: string,
  category: string,
  extras: Omit<MerchantCatalogDef, 'name' | 'slug' | 'primaryCategoryCode'> = {},
): MerchantCatalogDef {
  return {
    name,
    slug: slugify(name),
    primaryCategoryCode: category,
    brandName: extras.brandName ?? name,
    ...extras,
  };
}

function fromNames(names: string[], category: string, baseScore = 40): MerchantCatalogDef[] {
  return names.map((name, index) =>
    m(name, category, {
      popularityScore: Math.max(5, baseScore - Math.floor(index / 5)),
      tags: [category.toLowerCase()],
    }),
  );
}

