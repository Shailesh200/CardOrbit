/** Regional outlet expansion — unique slugs for coverage depth without duplicating flagship brands. */
function regionalOutlets(
  brands: string[],
  cities: string[],
  category: string,
): MerchantCatalogDef[] {
  const rows: MerchantCatalogDef[] = [];
  for (const brand of brands) {
    for (const city of cities) {
      const name = `${brand} ${city}`;
      rows.push(
        m(name, category, {
          brandName: brand,
          parentBrand: brand,
          tags: ['regional-outlet', category.toLowerCase()],
          popularityScore: 20,
        }),
      );
    }
  }
  return rows;
}

